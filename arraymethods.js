// array method 

// push 
// pop
// shift 
// unshift 
// sort 
// reverse 

// push --> is use to push a value in the end
let pu =[10,20,30,40,50]
pu.push(60)
console.log(pu);

// pop --> is use to remove the value in the end

let po =[10,20,30,40,50]
po.pop()
console.log(po);

// shift --> is use to remove the value in the frount

let shi =[10,20,30,40,50]
shi.shift()
console.log(shi);

// unshift --> is use to add a value in the frount


let un =[10,20,30,40,50]
un.unshift (60)
console.log(un);

// sort --> is use to give the value in alfabetic order

let so =["banana","orange","apple","mango","graps","goa"]
console.log(so.sort());

// reverse is use to reverse the given value

let rev = "htijnar"
console.log(rev.split("").reverse().join(""));
